    private void jTextFieldBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldBuscarKeyReleased
        // TODO add your handling code here:
        String busqueda = jTextFieldBuscar.getText();
        
        mBusqueda.clear();
        
        for (int i=0; i<mProducto.size(); i++){
            if (mProducto.get(i).toLowerCase().startsWith(busqueda.toLowerCase())){
                
             mBusqueda.addElement(mProducto.get(i));   
                
            }
        }
        jListProductos.setModel(mBusqueda);
        
        
    }//GEN-LAST:event_jTextFieldBuscarKeyReleased

